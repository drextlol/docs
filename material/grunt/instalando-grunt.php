<h3>Instalando as bibliotecas do grunt no projeto</h3>

<p>
	Caso o projeto contenha os arquivos package.json e gruntfile.js significa que está instalado o grunt, para isso basta abrir seu terminal e navegar até a pasta do projeto, feito isso execute o seguinte comando.
</p>

<div class="code">
	<span>// Baixando bibliotecas grunt do projeto</span>
	<div class="var">npm install</div>
